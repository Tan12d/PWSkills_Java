package Basics;

public class doubleDataType
{
    public static void main(String[] args)
    {
        double x = 3.1415;
        System.out.println(x);
        System.out.println(63);

        float y = 3.1415f;
        System.out.println(y);

        System.out.println(x+y);

        int z = 7;

        System.out.println(x+y+z);
    }
}
