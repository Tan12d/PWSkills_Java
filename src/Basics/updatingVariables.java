package Basics;

public class updatingVariables
{
    public static void main(String[] args)
    {
        int a = 5;
        System.out.println(a);

        a = 7;
        System.out.println(a);

        a = a + 6;
        System.out.println(a);

        a = a - 2;
        System.out.println(a);
    }
}
