package Basics;

public class modulusOperator
{
    public static void main(String[] args) {
        int a = 8;
        int b = 2;

        System.out.println(a % b);

        System.out.println(3 % 23);

        System.out.println(5 % (-2));

        System.out.println((-5) % 2);

        System.out.println((-5)%(-2));
    }
}
